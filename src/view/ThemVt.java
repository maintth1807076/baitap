    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ten = txtTen.getText();
        String thuong = txtThuong.getText();
        
        if (ten.length() == 0)
            JOptionPane.showMessageDialog(rootPane, "Ten khong duoc de trong!");
        else if (thuong.length() == 0)
            JOptionPane.showMessageDialog(rootPane, "Thuong khong duoc de trong!");
        else if (thuong.matches("\\d+") == false)
            JOptionPane.showMessageDialog(rootPane, "Thuong khong dung dinh dang!");
        else {
            int thuongInt = Integer.parseInt(thuong);
            
            ViTri tmp = new ViTri(ten, thuongInt);
            home.themVt(tmp);
            JOptionPane.showMessageDialog(rootPane, "Them vi tri thanh cong!");
            txtTen.setText("");
            txtThuong.setText("");
        }
                
    }//GEN-LAST:event_jButton2ActionPerformed

