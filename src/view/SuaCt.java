    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String ma = txtMa.getText();

        if (ma.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Hay nhap ma cau thu!");
        } else if (ma.matches("\\d+") == false) {
            JOptionPane.showMessageDialog(rootPane, "Ma cau thu khong dung!");
        } else {
            boolean check = false;
            int maInt = Integer.parseInt(ma);
            CauThu cauThu = cauThuDAO.getCauThuByMa(maInt);
            if (cauThu != null) {
                check = true;
                txtTen.setText(cauThu.getHoten());
                txtTuoi.setText(String.valueOf(cauThu.getTuoi()));
                txtLuong.setText(String.valueOf(cauThu.getLuong()));

            }

            if (check == false) {
                JOptionPane.showMessageDialog(rootPane, "Khong tim thay cau thu!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

