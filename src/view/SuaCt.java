    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ma = txtMa.getText();
        int maInt = Integer.parseInt(ma);

        String ten = txtTen.getText();
        String tuoi = txtTuoi.getText();
        int tuoiInt = Integer.parseInt(tuoi);

        String luong = txtLuong.getText();
        int luongInt = Integer.parseInt(luong);

        CauThu cauThu = cauThuDAO.getCauThuByMa(maInt);
        if (cauThu != null) {
            cauThu.setHoten(ten);
            cauThu.setTuoi(tuoiInt);
            cauThu.setLuong(luongInt);
            cauThuDAO.updateCauThu(cauThu);
            home.hienThi(cauThuDAO.getCauThus(), home.getModelCt());
        }
        JOptionPane.showMessageDialog(rootPane, "Sua thanh cong!");
    }//GEN-LAST:event_jButton2ActionPerformed

